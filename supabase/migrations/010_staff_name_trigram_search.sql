-- =========================================================
-- スタッフ氏名検索の高速化: pg_trgm トライグラムインデックス
-- 前提: 001〜009 実行済み
-- =========================================================
--
-- /admin/staff の氏名検索(ilikeによる部分一致)は、スタッフ数が
-- 数百人規模になると素朴なテーブルスキャンでは遅くなってくる。
-- pg_trgm 拡張機能とGINインデックスにより、ilikeの部分一致検索
-- (前方・中間・後方一致すべて)を高速化する。
--
-- 【課題・特性(READMEにも記載)】
-- ・GINインデックスはbtreeよりinsert/update時の書き込みコストが高い
--   (スタッフ登録・編集の頻度は低いため実運用上は問題にならない想定)
-- ・トライグラムは3文字単位のn-gramで扱うため、1〜2文字の検索語には
--   絞り込み効果が薄い(動作はするが候補数が増える)
--
-- アプリ側のコード変更は不要。既存の `.ilike("name", "%...%")` が
-- そのままこのインデックスを利用できるようになる。

create extension if not exists pg_trgm;

create index if not exists idx_profiles_name_trgm
  on profiles using gin (name gin_trgm_ops);
